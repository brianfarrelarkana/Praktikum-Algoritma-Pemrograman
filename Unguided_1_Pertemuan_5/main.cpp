#include <iostream>

using namespace std;

int main()
{
    cout << "Menaik: " << endl;
    for (int hayo = 1; hayo <= 5; hayo++) {
        cout << hayo << endl;
    }

    cout << "\nMenurun: " << endl;
    for (int hayo = 5; hayo >= 1; hayo--) {
        cout << hayo << endl;
    }

    return 0;
}
